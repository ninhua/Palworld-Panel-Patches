#!/usr/bin/env bash
set -Eeuo pipefail
umask 022

usage() {
    echo "用法：$0 <上游源码目录> <报告输出目录>" >&2
    exit 2
}

[[ $# -eq 2 ]] || usage

src="$(cd "$1" && pwd)"
report="$2"
mkdir -p "${report}"

git -C "${src}" rev-parse HEAD > "${report}/commit.txt"
git -C "${src}" log -1 \
    --format='commit=%H%nparents=%P%nauthor=%an <%ae>%nauthor_date=%aI%ncommitter=%cn <%ce>%ncommit_date=%cI%nsubject=%s' \
    > "${report}/commit-details.txt"
git -C "${src}" status --short > "${report}/git-status.txt"
git -C "${src}" submodule status --recursive > "${report}/submodules.txt" 2>&1 || true
git -C "${src}" remote -v > "${report}/remotes.txt"

{
    echo "# top-level"
    find "${src}" -mindepth 1 -maxdepth 1 -printf '%f\n' | LC_ALL=C sort
    echo
    echo "# files up to depth 4"
    find "${src}" -mindepth 1 -maxdepth 4 \
        -not -path '*/.git/*' \
        -printf '%y %P\n' | LC_ALL=C sort
} > "${report}/tree.txt"

find "${src}" -type f \
    -not -path '*/.git/*' \
    \( \
        -name 'go.mod' -o \
        -name 'go.work' -o \
        -name 'go.sum' -o \
        -name 'package.json' -o \
        -name 'package-lock.json' -o \
        -name 'pnpm-lock.yaml' -o \
        -name 'yarn.lock' -o \
        -name 'Makefile' -o \
        -name 'Dockerfile*' -o \
        -name '*.csproj' -o \
        -name '*.sln' \
    \) \
    -printf '%P\n' | LC_ALL=C sort > "${report}/build-files.txt"

find "${src}" -type f -name '*.go' \
    -not -path '*/vendor/*' \
    -not -path '*/.git/*' \
    -printf '%P\n' | LC_ALL=C sort > "${report}/go-files.txt"

find "${src}" -type f \
    \( -name 'main.go' -o -name 'routes.go' -o -name 'router.go' -o -name 'server.go' -o -name 'app.go' \) \
    -not -path '*/vendor/*' \
    -printf '%P\n' | LC_ALL=C sort > "${report}/entry-candidates.txt"

grep -RInE \
    --include='*.go' \
    --exclude-dir=.git \
    --exclude-dir=vendor \
    '(gin\.(Default|New)|echo\.New|fiber\.New|chi\.NewRouter|http\.(Handle|HandleFunc|ListenAndServe)|\.(GET|POST|PUT|PATCH|DELETE|Group)\s*\()' \
    "${src}" > "${report}/routing-candidates.txt" || true

grep -RInE \
    --include='*.go' \
    --exclude-dir=.git \
    --exclude-dir=vendor \
    '(embed\.FS|//go:embed|http\.FileServer|StaticFS|Static\(|NoRoute|ServeFile)' \
    "${src}" > "${report}/static-embed-candidates.txt" || true

grep -RInE \
    --include='*.go' \
    --exclude-dir=.git \
    --exclude-dir=vendor \
    '(auth|middleware|JWT|session|permission|authorize)' \
    "${src}" > "${report}/auth-candidates.txt" || true

grep -RInE \
    --include='*.go' \
    --exclude-dir=.git \
    --exclude-dir=vendor \
    '(/api|api/|API)' \
    "${src}" > "${report}/api-candidates.txt" || true

{
    echo "# Go"
    command -v go || true
    go version 2>&1 || true
    go env 2>&1 || true

    echo
    echo "# Node"
    command -v node || true
    node --version 2>&1 || true
    command -v npm || true
    npm --version 2>&1 || true

    echo
    echo "# .NET"
    command -v dotnet || true
    dotnet --info 2>&1 || true
} > "${report}/toolchain.txt"

if [[ -f "${src}/go.mod" ]]; then
    cp "${src}/go.mod" "${report}/go.mod"
fi
if [[ -f "${src}/go.work" ]]; then
    cp "${src}/go.work" "${report}/go.work"
fi
if [[ -f "${src}/Makefile" ]]; then
    cp "${src}/Makefile" "${report}/Makefile"
fi

while IFS= read -r package_json; do
    relative="${package_json#${src}/}"
    safe_name="${relative//\//__}"
    cp "${package_json}" "${report}/${safe_name}"
    (
        cd "$(dirname "${package_json}")"
        npm pkg get scripts 2>&1 || true
    ) > "${report}/${safe_name}.scripts.txt"
done < <(
    find "${src}" -type f -name package.json \
        -not -path '*/node_modules/*' \
        -not -path '*/.git/*' \
        | LC_ALL=C sort
)

python3 - "${src}" "${report}" <<'PY'
from __future__ import annotations

import hashlib
import json
import subprocess
import sys
from pathlib import Path

src = Path(sys.argv[1])
report = Path(sys.argv[2])
commit = (report / "commit.txt").read_text(encoding="utf-8").strip()

def git(*args: str) -> str:
    return subprocess.check_output(
        ["git", "-C", str(src), *args],
        text=True,
    ).strip()

payload = {
    "schema_version": 1,
    "project": "uitok-palworld-panel",
    "repository": "uitok/palworld-panel",
    "source_ref": "dev",
    "source_commit": commit,
    "source_commit_date": git("show", "-s", "--format=%cI", "HEAD"),
    "target_version": "v1.2.2",
    "compatibility_status": "unverified",
    "source_archive": {
        "filename": "",
        "sha256": "",
    },
    "notes": "Generated by Probe uitok dev source workflow.",
}

(report / "upstream-lock.generated.json").write_text(
    json.dumps(payload, ensure_ascii=False, indent=2) + "\n",
    encoding="utf-8",
)
PY

echo "Upstream probe completed: ${report}"
